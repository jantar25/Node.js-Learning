const os = require("os");
//Get os Platform
console.log(os.platform());

//Get CPU Arch
console.log(os.arch());
//Get CPU core info
console.log(os.cpus());
//Get free memory
console.log(os.freemem());
//Get total memory
console.log(os.totalmem());
//Get home directory
console.log(os.homedir());
//Get os uptime
console.log(os.uptime());
